    public void getData(){
    model.getDataVector().removeAllElements();
    model.fireTableDataChanged();
    
    try {
    Statement stat = (Statement)koneksi.KoneksiDB().createStatement();
    String sql = "SELECT * FROM users";
    ResultSet res=stat.executeQuery(sql);
    
    while (res.next()){
    Object[] obj=new Object[5];
    obj[0]=res.getString("username");
    obj[1]=res.getString("nama_lengkap");
    obj[2]=res.getString("email");
    obj[3]=res.getString("no_hp");
    obj[4]=res.getString("password");

    model.addRow(obj);
    }
    }
    catch (SQLException err){
                JOptionPane.showMessageDialog(null,err.getMessage());
        }
    }
    
    public void loadData(){
        username=jUser.getText();
        nama_lengkap=jNama.getText();
        email=jEmail.getText();
        no_hp=jHP.getText();
        password=jPass.getText();
    }
    
    public void saveData(){
        loadData();
        try {
         Statement stat = (Statement) koneksi.KoneksiDB().createStatement();
         String sql = "INSERT INTO users (username, nama_lengkap, email, no_hp, password)"
                 + "VALUES ('"+username+"','"+nama_lengkap+"','"+email+"','"+no_hp+"','"+password+"')";
        PreparedStatement p = (PreparedStatement)
                koneksi.KoneksiDB().prepareStatement(sql);
        p.executeUpdate();
        getData();
        }catch (SQLException er){
            JOptionPane.showMessageDialog(null, er.getMessage());
        }
    }
    
