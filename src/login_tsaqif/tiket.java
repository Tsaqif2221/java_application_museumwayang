    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if (jComboBox1.getSelectedItem()=="-Silahkan Pilih-")
        {
            jTextField2.setText("");
        }
        else if (jComboBox1.getSelectedItem()=="Tiket Masuk+Teater")
        {
            jTextField2.setText("50000");
        }
        else if (jComboBox1.getSelectedItem()=="Tiket Masuk")
        {
            jTextField2.setText("25000");
        }
        else {
        jTextField2.setText("15000");
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

