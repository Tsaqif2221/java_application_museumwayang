    public void updateData(){
        loadData();
        try {
        Statement stat = (Statement)koneksi.KoneksiDB().createStatement();
        String sql = "UPDATE tiket SET jenis = '"+jenis_tiket+"',"+" harga = '"+harga+"',"+"jumlah='"+jumlah_tiket+"',"+" total = '"+total+"'WHERE nama='"+nama_pemesan+"'";
        PreparedStatement p =(PreparedStatement)koneksi.KoneksiDB().prepareStatement(sql);
        p.executeUpdate();
        getData();
        Reset ();
        JOptionPane.showMessageDialog(null, "Update berhasil...");
        
        }catch (SQLException er){
            JOptionPane.showMessageDialog(null, er.getMessage());
        }
    }
    
    public void deleteData(){
        loadData();
        int pesan=JOptionPane.showConfirmDialog(null, "Anda yakin menghapus data"+nama_pemesan+"?","Konfirmasi",JOptionPane.OK_CANCEL_OPTION);
        
        if (pesan==JOptionPane.OK_OPTION){
            try{
                Statement stat=(Statement)koneksi.KoneksiDB().createStatement();
                String sql="DELETE FROM tiket WHERE nama='"+nama_pemesan+"'";
                
                PreparedStatement p=(PreparedStatement)koneksi.KoneksiDB().prepareStatement(sql);
                p.executeUpdate();
                getData();
                Reset();
                JOptionPane.showMessageDialog(null, "Delete berhasil");
            }catch (SQLException er){
                JOptionPane.showMessageDialog(null, er.getMessage());
            }
        }
    }
    
    
